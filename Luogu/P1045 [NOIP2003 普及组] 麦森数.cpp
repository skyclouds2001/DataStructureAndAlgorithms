#include<bits/stdc++.h>
using namespace std;
#define SIZE 2000
int p,a[SIZE];
int b[SIZE],c[SIZE];	//SIZE:memset,memcpy !=sizeof(a/b/c)
int main()
{
	cin>>p;
	//p=3100000; 
	cout<<(int)(p*log10(2)+1)<<endl;
	b[1]=2;	//f
	a[1]=1;	//res
	for(int k=p;k>0;k>>=1)
	{
		if(k&1)
		{
			memset(c,0,SIZE*sizeof(int));	//c:sav  b:f  a:res
			for(int j=1;j<=500;++j)
				for(int i=1;i<=500;++i)
					c[i+j-1]+=a[i]*b[j];
			for(int j=1;j<=999;++j)
				c[j+1]+=c[j]/10,c[j]%=10;
			memcpy(a,c,SIZE*sizeof(int));
		}
		memset(c,0,SIZE*sizeof(int));
		for(int j=1;j<=999;++j)
				for(int i=1;i<=999;++i)
					c[i+j-1]+=b[i]*b[j];
		memset(b,0,SIZE*sizeof(int));
		for(int j=1;j<=999;++j)
			c[j+1]+=c[j]/10,c[j]%=10;
		memcpy(b,c,SIZE*sizeof(int));
	}
	--a[1];
	for(int j=500;j>=1;--j)
		if((j-1)%50==0)
			cout<<a[j]<<endl;
		else
			cout<<a[j];
	return 0;
}
